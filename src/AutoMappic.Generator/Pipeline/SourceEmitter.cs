                sb.AppendLine("                }");
                sb.AppendLine("                foreach (var tItem in __staleItems) mutableTarget.Remove(tItem);");
                sb.AppendLine("            }");
                sb.AppendLine("        }");
                if (!prop.IsReadOnly)
                {
                    sb.AppendLine("        }");
                    sb.AppendLine("        else");
                    sb.AppendLine("        {");
                    sb.AppendLine($"            result.{prop.DestinationProperty} = {expression};");
                    sb.AppendLine("        }");
                }
