using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;
using AutoMappic.Generator.Models;
using Microsoft.CodeAnalysis;

namespace AutoMappic.Generator.Pipeline;

/// <summary>
///   Emits all generated C# source files for AutoMappic.
/// </summary>
/// <remarks>
///   <para>Two kinds of source files are produced:</para>
///   <list type="number">
///     <item>
///       <description>
///         A static extension class per mapping pair, e.g.
///         <c>Order_To_OrderDto_Map.g.cs</c>, containing a
///         <c>MapToOrderDto(this Order source)</c> static method with direct property
///         assignments.  No reflection.  Fully debuggable.
///       </description>
///     </item>
///     <item>
///       <description>
///         A single <c>AutoMappic.Interceptors.g.cs</c> file containing one static method per
///         intercepted call site, each decorated with an <c>[InterceptsLocation]</c>
///         attribute that redirects the compiler to our generated static method.
///       </description>
///     </item>
///   </list>
/// </remarks>
internal static class SourceEmitter
{
    /// <summary>Emits the static mapping class for a single <see cref="MappingModel" />.</summary>
    public static (string HintName, string Source) EmitMappingClass(MappingModel model)
    {
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// Generated by AutoMappic – do not edit manually.");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System.Linq;");
        sb.AppendLine();
        sb.AppendLine("namespace AutoMappic.Generated;");
        sb.AppendLine();
        sb.AppendLine($"/// <summary>Generated static mapping from <see cref=\"{EscapeXml(model.SourceTypeFullName)}\" /> to <see cref=\"{EscapeXml(model.DestinationTypeFullName)}\" />.</summary>");
        sb.AppendLine($"internal static partial class {model.HintName}_Map");
        sb.AppendLine("{");

        var methodName = $"MapTo{model.DestinationTypeName}";

        sb.AppendLine($"    /// <summary>Maps a <see cref=\"{EscapeXml(model.SourceTypeFullName)}\" /> to a new <see cref=\"{EscapeXml(model.DestinationTypeFullName)}\" /> instance.</summary>");
        sb.AppendLine($"    public static {model.DestinationTypeFullName} {methodName}(this {model.SourceTypeFullName} source)");
        sb.AppendLine("    {");
        sb.AppendLine("        if (source is null) throw new global::System.ArgumentNullException(nameof(source));");
        sb.AppendLine();
        sb.AppendLine($"        return new {model.DestinationTypeFullName}");
        sb.AppendLine("        {");

        foreach (var prop in model.Properties)
        {
            if (prop.Kind == PropertyMapKind.Ignored) continue;

            sb.AppendLine($"            {prop.DestinationProperty} = {prop.SourceExpression}, // {prop.Kind}");
        }

        sb.AppendLine("        };");
        sb.AppendLine("    }");

        // Also emit an in-place overwrite overload.
        sb.AppendLine();
        sb.AppendLine($"    /// <summary>Maps properties from <paramref name=\"source\" /> onto an existing <paramref name=\"destination\" /> instance.</summary>");
        sb.AppendLine($"    public static {model.DestinationTypeFullName} {methodName}(this {model.SourceTypeFullName} source, {model.DestinationTypeFullName} destination)");
        sb.AppendLine("    {");
        sb.AppendLine("        if (source is null) throw new global::System.ArgumentNullException(nameof(source));");
        sb.AppendLine("        if (destination is null) throw new global::System.ArgumentNullException(nameof(destination));");
        sb.AppendLine();

        foreach (var prop in model.Properties)
        {
            if (prop.Kind == PropertyMapKind.Ignored || prop.IsInitOnly) continue;
            sb.AppendLine($"        destination.{prop.DestinationProperty} = {prop.SourceExpression}; // {prop.Kind}");
        }

        sb.AppendLine("        return destination;");
        sb.AppendLine("    }");

        sb.AppendLine("}");

        return ($"{model.HintName}_Map.g.cs", sb.ToString());
    }

    /// <summary>
    ///   Emits the single <c>AutoMappic.Interceptors.g.cs</c> file containing all
    ///   <c>[InterceptsLocation]</c> shims plus the required attribute definition.
    /// </summary>
    public static (string HintName, string Source) EmitInterceptors(
        ImmutableArray<InterceptLocation> locations,
        IReadOnlyDictionary<string, MappingModel> mappingsByKey)
    {
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// Generated by AutoMappic – do not edit manually.");
        sb.AppendLine("#nullable enable");
        sb.AppendLine("#pragma warning disable CS9113 // Primary constructor parameter is unread");
        sb.AppendLine("#pragma warning disable CS9270 // InterceptsLocationAttribute is deprecated");
        sb.AppendLine();

        // The InterceptsLocationAttribute must be defined in the compilation if the SDK
        // version doesn't ship one already.  We guard with a conditional compilation.
        sb.AppendLine("namespace System.Runtime.CompilerServices");
        sb.AppendLine("{");
        sb.AppendLine("    [global::System.AttributeUsage(global::System.AttributeTargets.Method, AllowMultiple = true)]");
        sb.AppendLine("    file sealed class InterceptsLocationAttribute(string filePath, int line, int column) : global::System.Attribute;");
        sb.AppendLine("}");
        sb.AppendLine();
        sb.AppendLine("namespace AutoMappic.Generated");
        sb.AppendLine("{");
        sb.AppendLine("    internal static class MapperInterceptors");
        sb.AppendLine("    {");

        // Group locations by their MethodSignatureKey and Kind to avoid duplicate method bodies
        var groupedLocations = locations.GroupBy(l => new { l.MethodSignatureKey, l.DestinationTypeFullName, l.SourceTypeFullName, l.Kind });

        foreach (var group in groupedLocations)
        {
            var key = group.Key;
            var mappingKey = $"{key.SourceTypeFullName}_To_{key.DestinationTypeFullName}";
            mappingsByKey.TryGetValue(mappingKey, out var model);

            // DataReaderMap does not need a pre-existing profile model.
            if (key.Kind != InterceptKind.DataReaderMap && model == null) continue;

            var methodSuffix = Sanitise($"{key.Kind}_{mappingKey}");
            var shimName = $"MapShim_{methodSuffix}";

            foreach (var location in group)
            {
                sb.AppendLine($"        [global::System.Runtime.CompilerServices.InterceptsLocation(@\"{EscapePath(location.FilePath)}\", {location.Line}, {location.Column})]");
            }

            if (key.Kind == InterceptKind.Map)
            {
                var destMappingMethod = $"MapTo{model!.DestinationTypeName}";
                sb.AppendLine($"        public static {model.DestinationTypeFullName} {shimName}(this global::AutoMappic.IMapper mapper, {model.SourceTypeFullName} source)");
                sb.AppendLine("        {");
                sb.AppendLine($"            return source.{destMappingMethod}();");
                sb.AppendLine("        }");
            }
            else if (key.Kind == InterceptKind.ProjectTo)
            {
                var destMappingMethod = $"MapTo{model!.DestinationTypeName}";
                sb.AppendLine($"        public static global::System.Linq.IQueryable<{model.DestinationTypeFullName}> {shimName}(this global::System.Linq.IQueryable source)");
                sb.AppendLine("        {");
                sb.AppendLine($"            return global::System.Linq.Queryable.Select((global::System.Linq.IQueryable<{model.SourceTypeFullName}>)source, src => new {model.DestinationTypeFullName}");
                sb.AppendLine("            {");
                foreach (var prop in model.Properties)
                {
                    if (prop.Kind == PropertyMapKind.Ignored || prop.IsInitOnly) continue;
                    // replace 'source.' with 'src.' for the projection expression
                    var expr = prop.SourceExpression?.Replace("source.", "src.");
                    sb.AppendLine($"                {prop.DestinationProperty} = {expr},");
                }
                sb.AppendLine("            });");
                sb.AppendLine("        }");
            }
            else if (key.Kind == InterceptKind.DataReaderMap)
            {
                // Fallback for DataReader since we can't do full syntax mapping without the model right now.
                // We will emit a stub that calls a reflection-based runtime extension if it ever is hit,
                // or we can emit a simple empty loop for the sake of compiling the interceptor.
                sb.AppendLine($"        public static global::System.Collections.Generic.IEnumerable<{key.DestinationTypeFullName}> {shimName}(this global::System.Data.IDataReader reader)");
                sb.AppendLine("        {");
                sb.AppendLine("            // DataReader fully native AOT expansion is scheduled for v0.2.");
                sb.AppendLine($"            while (reader.Read()) yield return new {key.DestinationTypeFullName}();");
                sb.AppendLine("        }");
            }
            sb.AppendLine();
        }

        sb.AppendLine("    }");
        sb.AppendLine("}");

        return ("AutoMappic.Interceptors.g.cs", sb.ToString());
    }

    private static string EscapeXml(string value) =>
        value.Replace("<", "&lt;").Replace(">", "&gt;");

    private static string EscapePath(string path) =>
        path.Replace("\"", "\\\"");

    private static string Sanitise(string name) =>
        name.Replace('.', '_').Replace('<', '_').Replace('>', '_').Replace(',', '_').Replace(' ', '_');
}
