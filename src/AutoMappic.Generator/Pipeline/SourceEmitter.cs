    private static void CollectCollectionHelpers(MappingModel model, List<PropertyMap> collectionHelpers)
    {
        foreach (var arg in model.ConstructorArguments.Where(a => a.IsCollection))
            collectionHelpers.Add(arg with { DestinationProperty = arg.DestinationProperty + "_Ctor" });

        foreach (var prop in model.Properties.Where(p => p.IsCollection && p.NestedSourceTypeFullName != null && p.Kind != PropertyMapKind.Ignored))
            collectionHelpers.Add(prop);
    }
    private static void EmitCollectionHelper(StringBuilder sb, PropertyMap helper)
    {
        var sItem = helper.NestedSourceTypeFullName!;
        var dItem = helper.NestedDestTypeFullName!;
        var dType = helper.NestedFullDestTypeFullName ?? (helper.IsArray ? $"{dItem}[]" : $"global::System.Collections.Generic.List<{dItem}>");
        var returnExpr = helper.IsArray ? "list.ToArray()" : "list";

        if (helper.NestedFullDestTypeFullName != null && helper.NestedFullDestTypeFullName.Contains("HashSet<"))
            returnExpr = "list.ToHashSet()";
        else if (helper.NestedFullDestTypeFullName != null && helper.NestedFullDestTypeFullName.Contains("Stack<"))
            returnExpr = "new " + helper.NestedFullDestTypeFullName + "(list)";
        else if (helper.NestedFullDestTypeFullName != null && helper.NestedFullDestTypeFullName.Contains("Queue<"))
            returnExpr = "new " + helper.NestedFullDestTypeFullName + "(list)";
        var dName = dItem;
        var innerLogic = helper.NestedExpression ?? $"x.MapTo{Sanitise(dName, true)}(context)";

        var dNull = helper.IsArray ? $"global::System.Array.Empty<{dItem}>()" : $"new {dType}(0)";
        if (dType.Contains("HashSet")) dNull = $"new {dType}()";
        else if (dType.Contains("Stack")) dNull = $"new {dType}()";
        else if (dType.Contains("Queue")) dNull = $"new {dType}()";
        else if (helper.NestedFullDestTypeFullName != null && !helper.IsArray)
        {
            // If dType is an interface, we must instantiate a concrete type
            if (dType.Contains("IReadOnlyList") || dType.Contains("IList") || dType.Contains("IEnumerable") || dType.Contains("ICollection"))
                dNull = $"new global::System.Collections.Generic.List<{dItem}>(0)";
            else
                dNull = $"new {dType}()";
        }

        sb.AppendLine();
        sb.AppendLine($"    private static {dType} MapCollection_{helper.DestinationProperty}(global::System.Collections.Generic.IEnumerable<{sItem}>? source, global::AutoMappic.Generated.MappingContext context = default)");
        sb.AppendLine("    {");
        sb.AppendLine($"        if (source is null) return {dNull};");
        sb.AppendLine($"        var list = source switch {{ global::System.Collections.Generic.ICollection<{sItem}> generic => new global::System.Collections.Generic.List<{dItem}>(generic.Count), global::System.Collections.ICollection nonGeneric => new global::System.Collections.Generic.List<{dItem}>(nonGeneric.Count), _ => new global::System.Collections.Generic.List<{dItem}>() }};");
        sb.AppendLine($"        if (source is global::System.Collections.Generic.IList<{sItem}> listSource)");
        sb.AppendLine("        {");
        sb.AppendLine("            var count = listSource.Count;");
        sb.AppendLine("            for (var i = 0; i < count; i++)");
        sb.AppendLine("            {");
        sb.AppendLine("                var x = listSource[i];");
        sb.AppendLine($"                list.Add({innerLogic});");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine("        else");
        sb.AppendLine("        {");
        sb.AppendLine("            foreach (var x in source)");
        sb.AppendLine("            {");
        sb.AppendLine($"                list.Add({innerLogic});");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine($"        return {returnExpr};");
        sb.AppendLine("    }");
    }

