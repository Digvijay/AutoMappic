    public static (string HintName, string Source) EmitRegistration(string assemblyName, ImmutableArray<string?> profiles, ImmutableArray<MappingModel> localMappings, ImmutableArray<string> referencedAssemblies, bool isEntryPoint)
    {
        var sb = new StringBuilder();
        var sanitized = Sanitise(assemblyName, false);
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#pragma warning disable CS1591");
        sb.AppendLine("#nullable enable");
        sb.AppendLine("using Microsoft.Extensions.DependencyInjection;");
        foreach (var m in localMappings)
        {
            // CS0616/AM0012 safety: only emit discovery for mappings that have properties.
            // Mapping from object to UnmappedDest has no properties, causing CS0616 if UnmappedDest is used in discovery.
            bool hasValidTarget = m.Properties.Any(p => p.Kind != Models.PropertyMapKind.Ignored) ||
                                  m.ConstructorArguments.Any(p => p.Kind != Models.PropertyMapKind.Ignored) ||
                                  !string.IsNullOrEmpty(m.TypeConverterFullName) ||
                                  !string.IsNullOrEmpty(m.StaticConverterMethodFullName) ||
                                  !string.IsNullOrEmpty(m.ConstructionBody);

            if (!hasValidTarget)
            {
                continue;
            }

            var src = GetUnbound(m.SourceTypeFullName);
            var dest = GetUnbound(m.DestinationTypeFullName);
            // Only emit global:: references. Plain 'S' or 'D' (type parameters) should be skipped.
            if (src.StartsWith("global::", StringComparison.Ordinal) && !src.EndsWith("::S", StringComparison.Ordinal) && !src.EndsWith("::D", StringComparison.Ordinal) &&
                dest.StartsWith("global::", StringComparison.Ordinal) && !dest.EndsWith("::S", StringComparison.Ordinal) && !dest.EndsWith("::D", StringComparison.Ordinal) &&
                !src.Equals("global::S", StringComparison.Ordinal) && !src.Equals("global::D", StringComparison.Ordinal) &&
                !dest.Equals("global::S", StringComparison.Ordinal) && !dest.Equals("global::D", StringComparison.Ordinal))
            {
                sb.AppendLine($"[assembly: global::AutoMappic.MappingDiscoveryAttribute(typeof({src}), typeof({dest}))]");
            }
        }
        var pList = profiles.IsDefaultOrEmpty ? "" : string.Join(", ", profiles.Distinct().Where(p => p != null).Select(p => $"typeof({(p!.StartsWith("global::", StringComparison.Ordinal) ? p : $"global::{p}")})"));
        sb.AppendLine($"[assembly: global::AutoMappic.HasAutoMappicProfilesAttribute({pList})]");
        sb.AppendLine();
        sb.AppendLine("namespace AutoMappic.Generated");
        sb.AppendLine("{");
        sb.AppendLine($"    [global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Never)]");
        sb.AppendLine($"    internal sealed class Marker_{sanitized} {{ }}");
        sb.AppendLine();
        sb.AppendLine($"    [global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Never)]");
        sb.AppendLine($"    internal static class {sanitized}_Registration");
        sb.AppendLine("    {");
        sb.AppendLine("        public static void AddProfiles(global::Microsoft.Extensions.DependencyInjection.IServiceCollection services)");
        sb.AppendLine("        {");
        sb.AppendLine($"            if (global::System.Linq.Enumerable.Any(services, d => d.ServiceType == typeof(global::AutoMappic.Generated.Marker_{sanitized}))) return;");
        sb.AppendLine($"            services.AddSingleton<global::AutoMappic.Generated.Marker_{sanitized}>();");
        sb.AppendLine();
        foreach (var r in referencedAssemblies.Distinct()) sb.AppendLine($"            global::AutoMappic.Generated.{Sanitise(r, false)}_Registration.AddProfiles(services);");
        foreach (var p in profiles.Distinct().Where(x => x != null)) sb.AppendLine($"            services.AddSingleton<global::AutoMappic.Profile, global::{p}>();");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine("}");
        sb.AppendLine("namespace Microsoft.Extensions.DependencyInjection");
        sb.AppendLine("{");
        sb.AppendLine($"    public static class AutoMappic_Extension_{sanitized}");
        sb.AppendLine("    {");
        sb.AppendLine($"        public static IServiceCollection AddAutoMappicFrom{sanitized}(this IServiceCollection services) {{ global::AutoMappic.Generated.{sanitized}_Registration.AddProfiles(services); if (!global::System.Linq.Enumerable.Any(services, d => d.ServiceType == typeof(global::AutoMappic.IMapper))) services.AddSingleton<global::AutoMappic.IMapper>(sp => new global::AutoMappic.MapperConfiguration(cfg => {{ foreach(var p in sp.GetServices<global::AutoMappic.Profile>()) cfg.AddProfile(p); }}).CreateMapper()); return services; }}");
        if (isEntryPoint) sb.AppendLine("        public static IServiceCollection AddAutoMappic(this IServiceCollection services) => AddAutoMappicFrom" + sanitized + "(services);");
        sb.AppendLine("    }");
        sb.AppendLine("}");
        return ("AutoMappic.Registration.g.cs", sb.ToString());
    }

    private static string GetUsings(MappingModel model)
    {
        var usings = new HashSet<string>();
        if (!string.IsNullOrEmpty(model.SourceNamespace))
            usings.Add($"using {model.SourceNamespace};");
        if (!string.IsNullOrEmpty(model.DestinationNamespace))
            usings.Add($"using {model.DestinationNamespace};");
        if (model.EnablePerformanceProfiling)
            usings.Add("using System.Diagnostics;");
        return string.Join("\n", usings);
    }

